package com.wiley.DuoFinance.model;

public class Game {

    int gameId;
    int score;

    public Game() {
    }

    public int getGameId() {
        return gameId;
    }

    public int getScore() {
        return score;
    }

    public void setGameId(int gameId) {
        this.gameId = gameId;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
